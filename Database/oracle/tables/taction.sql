DROP TABLE ASU.TACTION CASCADE CONSTRAINTS
/

--
-- TACTION  (Table) 
--
CREATE TABLE ASU.TACTION
(
  FK_ID         NUMBER,
  FC_CAPTION    VARCHAR2(200 BYTE),
  FK_CLOB       NUMBER,
  FL_DEL        NUMBER,
  FC_ITERATIVE  VARCHAR2(20 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          16K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TACTION.FK_ID IS 'SEQUENCE=[SEQ_TACTION]'
/

COMMENT ON COLUMN ASU.TACTION.FC_CAPTION IS 'Заголовок события'
/

COMMENT ON COLUMN ASU.TACTION.FK_CLOB IS 'Ссылка на CLOB'
/

COMMENT ON COLUMN ASU.TACTION.FL_DEL IS 'Флаг: удален'
/

COMMENT ON COLUMN ASU.TACTION.FC_ITERATIVE IS 'Тип повторения.'
/


--
-- TACTION_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TACTION (Table)
--
CREATE OR REPLACE TRIGGER ASU."TACTION_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON ASU.TACTION  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
Begin
  SELECT SEQ_TACTION.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
SHOW ERRORS;


