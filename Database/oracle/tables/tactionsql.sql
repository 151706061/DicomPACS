--
-- TACTIONSQL  (Table) 
--
CREATE TABLE ASU.TACTIONSQL
(
  FK_ID       NUMBER,
  FC_CAPTION  VARCHAR2(200 BYTE),
  FC_SQL      VARCHAR2(4000 BYTE),
  FL_DEL      NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TACTIONSQL.FK_ID IS 'SEQUENCE=[SEQ_TACTIONSQL]'
/

COMMENT ON COLUMN ASU.TACTIONSQL.FC_CAPTION IS 'Название запроса'
/

COMMENT ON COLUMN ASU.TACTIONSQL.FC_SQL IS 'Текст запроса'
/

COMMENT ON COLUMN ASU.TACTIONSQL.FL_DEL IS 'Флаг удаления'
/


--
-- TACTIONSQL_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TACTIONSQL (Table)
--
CREATE OR REPLACE TRIGGER ASU."TACTIONSQL_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON ASU.TACTIONSQL  REFERENCING OLD AS Old NEW AS New
 FOR EACH ROW
Begin
  SELECT SEQ_TACTIONSQL.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
