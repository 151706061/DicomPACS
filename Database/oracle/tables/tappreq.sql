--
-- TAPPREQ  (Table) 
--
CREATE TABLE ASU.TAPPREQ
(
  FK_ID     NUMBER(15),
  FK_APPID  NUMBER(15),
  FK_REQID  NUMBER(15)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TAPPREQ IS 'Таблица соответствия программы и необходимых для ее работы файлов by TimurLan'
/

COMMENT ON COLUMN ASU.TAPPREQ.FK_ID IS 'SEQUENCE=[SEQ_TAPP]'
/

COMMENT ON COLUMN ASU.TAPPREQ.FK_APPID IS 'TAPP.FK_ID код модуля'
/

COMMENT ON COLUMN ASU.TAPPREQ.FK_REQID IS 'TAPP.FK_ID код необходимого файла'
/


--
-- TAPPREQ_INSERT  (Trigger) 
--
--  Dependencies: 
--   TAPPREQ (Table)
--
CREATE OR REPLACE TRIGGER ASU."TAPPREQ_INSERT" 
  BEFORE INSERT
  ON ASU.TAPPREQ   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  SELECT SEQ_TAPP.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
