CREATE TABLE ASU.TCOMMENT_RESULT
(
  FK_ID         NUMBER(15),
  FC_PARAM      VARCHAR2(200 BYTE),
  FN_VALUE      NUMBER(10),
  FN_PRECISION  NUMBER(5),
  FC_TYPE       VARCHAR2(100 BYTE),
  FC_VARCHAR    VARCHAR2(2000 BYTE),
  FL_CHECK      NUMBER(1)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          160K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
PARALLEL ( DEGREE DEFAULT INSTANCES DEFAULT )
MONITORING
/

