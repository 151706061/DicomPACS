/

COMMENT ON COLUMN ASU.TCOMMENT_RESULT.FK_ID IS 'SEQUENCE=[SEQ_TCOMMENT_RESULT]'
/


--
-- TCOMMENT_RESULT_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TCOMMENT_RESULT (Table)
--
CREATE OR REPLACE TRIGGER ASU."TCOMMENT_RESULT_BEFORE_INSERT" 
BEFORE INSERT 
ON tcomment_result
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
begin
 select seq_TCOMMENT_RESULT.NEXTVAL into :NEW.fk_id from dual;
end;
