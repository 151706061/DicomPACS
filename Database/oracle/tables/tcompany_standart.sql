--
-- TCOMPANY_STANDART  (Table) 
--
CREATE TABLE ASU.TCOMPANY_STANDART
(
  FK_ID               NUMBER                    NOT NULL,
  FK_COMPANYID        NUMBER,
  FK_STANDARTGROUPID  NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          80K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TCOMPANY_STANDART.FK_ID IS 'SEQUENCE=[SEQ_TCOMPANY_STANDART]'
/

COMMENT ON COLUMN ASU.TCOMPANY_STANDART.FK_COMPANYID IS 'TCOMPANY.FK_ID Код компании'
/

COMMENT ON COLUMN ASU.TCOMPANY_STANDART.FK_STANDARTGROUPID IS 'TSTANDART_GROUP.FK_ID Код группы стандарта'
/


--
-- TCOMP_STAND_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TCOMPANY_STANDART (Table)
--
CREATE OR REPLACE TRIGGER ASU."TCOMP_STAND_BEFORE_INSERT" 
  BEFORE INSERT
  ON ASU.TCOMPANY_STANDART   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  SELECT SEQ_TCOMPANY_STANDART.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
