--
-- TCONST_APP  (Table) 
--
CREATE TABLE ASU.TCONST_APP
(
  FK_ID        NUMBER                           NOT NULL,
  FK_APP_ID    NUMBER,
  FK_CONST_ID  NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TCONST_APP IS 'Связь констант с приложениями, к которым они относятся AUTHOR:KOSOV  '
/

COMMENT ON COLUMN ASU.TCONST_APP.FK_ID IS 'SEQUENCE=[SEQ_TCONST_APP]'
/

COMMENT ON COLUMN ASU.TCONST_APP.FK_APP_ID IS 'LOGIN.TAPP.FK_ID'
/

COMMENT ON COLUMN ASU.TCONST_APP.FK_CONST_ID IS 'ASU.TCONST.FK_ID'
/


--
-- TCONST_APP_INSERT  (Trigger) 
--
--  Dependencies: 
--   TCONST_APP (Table)
--
CREATE OR REPLACE TRIGGER ASU."TCONST_APP_INSERT" 
  BEFORE INSERT ON ASU.TCONST_APP   FOR EACH ROW
BEGIN
  SELECT ASU.SEQ_TCONST_APP.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
