--
-- TDAYPLAN  (Table) 
--
CREATE TABLE ASU.TDAYPLAN
(
  FK_ID     NUMBER(15),
  FC_TIME1  VARCHAR2(5 BYTE),
  FC_TIME2  VARCHAR2(5 BYTE),
  FC_NAME   VARCHAR2(60 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          560K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TDAYPLAN.FK_ID IS 'SEQUENCE=[SEQ_TDAYPLAN]'
/


--
-- TDAYPLAN_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TDAYPLAN (Table)
--
CREATE OR REPLACE TRIGGER ASU."TDAYPLAN_BEFORE_INSERT" 
BEFORE INSERT
ON ASU.TDAYPLAN REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  SELECT SEQ_TDayPlan.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
