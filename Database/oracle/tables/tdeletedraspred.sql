CREATE TABLE ASU.TDELETEDRASPRED
(
  FK_ID       NUMBER                            NOT NULL,
  FD_DATE     DATE                              DEFAULT sysdate,
  FK_GRAPHID  NUMBER,
  FK_NAZID    NUMBER,
  FK_SOTRID   NUMBER,
  FC_COMMENT  VARCHAR2(4000 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

