/

COMMENT ON COLUMN ASU.TDELETEDRASPRED.FK_NAZID IS 'VNAZ.FK_ID'
/

COMMENT ON COLUMN ASU.TDELETEDRASPRED.FK_SOTRID IS 'TSOTR.FK_ID'
/


--
-- TDELETEDRASPRED_INSERT  (Trigger) 
--
--  Dependencies: 
--   TDELETEDRASPRED (Table)
--
CREATE OR REPLACE TRIGGER ASU."TDELETEDRASPRED_INSERT" 
  BEFORE INSERT
  ON ASU.TDELETEDRASPRED   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  select SEQ_DELETEDRASPRED.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
