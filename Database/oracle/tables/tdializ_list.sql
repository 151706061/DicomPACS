--
-- TDIALIZ_LIST_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TDIALIZ_LIST (Table)
--
CREATE OR REPLACE TRIGGER ASU."TDIALIZ_LIST_BEFORE_INSERT"
  BEFORE INSERT
  ON ASU.TDIALIZ_LIST   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
BEGIN
  SELECT ASU.SEQ_TDIALIZ_LIST.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
