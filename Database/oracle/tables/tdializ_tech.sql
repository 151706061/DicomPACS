--
-- TDIALIZ_TECH  (Table) 
--
CREATE TABLE ASU.TDIALIZ_TECH
(
  FK_ID              NUMBER                     NOT NULL,
  FK_DIALIZ_LIST_ID  NUMBER,
  FD_DATE            DATE,
  FC_JALOBY          VARCHAR2(500 BYTE),
  FC_SOST            VARCHAR2(500 BYTE),
  FC_TECHENIE        VARCHAR2(4000 BYTE),
  FK_SOTRID          NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TDIALIZ_TECH.FD_DATE IS 'дата'
/

COMMENT ON COLUMN ASU.TDIALIZ_TECH.FC_JALOBY IS 'жалобы'
/

COMMENT ON COLUMN ASU.TDIALIZ_TECH.FC_SOST IS 'состояние'
/

COMMENT ON COLUMN ASU.TDIALIZ_TECH.FC_TECHENIE IS 'течение диализа'
/

COMMENT ON COLUMN ASU.TDIALIZ_TECH.FK_SOTRID IS 'врач'
/


--
-- TDIALIZ_TECH_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TDIALIZ_TECH (Table)
--
CREATE OR REPLACE TRIGGER ASU."TDIALIZ_TECH_BEFORE_INSERT"
  BEFORE INSERT
  ON ASU.TDIALIZ_TECH   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
BEGIN
  SELECT ASU.SEQ_TDIALIZ_TECH.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
