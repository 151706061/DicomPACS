 BEFORE INSERT ON ASU.TDISTRICT  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
BEGIN
 IF :NEW.FK_ID IS NULL THEN
  SELECT SEQ_TDISTRICT.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
 END IF;
END;
