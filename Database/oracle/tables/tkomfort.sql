--
-- TKOMFORT  (Table) 
--
CREATE TABLE ASU.TKOMFORT
(
  FK_ID    NUMBER                               NOT NULL,
  FC_NAME  VARCHAR2(256 BYTE),
  FL_DEL   NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/


--
-- TKOMFORT_INSERT  (Trigger) 
--
--  Dependencies: 
--   TKOMFORT (Table)
--
CREATE OR REPLACE TRIGGER ASU."TKOMFORT_INSERT" 
  BEFORE INSERT
  ON ASU.TKOMFORT   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  select SEQ_TKOMFORT.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
