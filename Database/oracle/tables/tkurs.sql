--
-- TKURS_INS  (Trigger) 
--
--  Dependencies: 
--   TKURS (Table)
--
CREATE OR REPLACE TRIGGER ASU."TKURS_INS" 
 BEFORE 
 INSERT
 ON ASU.TKURS  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
Begin 
  select SEQ_TKURS.nextval into :new.KURSID from dual;
End;
