--
-- TLABLOT_PARAM  (Table) 
--
CREATE TABLE ASU.TLABLOT_PARAM
(
  FK_ID          NUMBER,
  FN_HEIGHT      NUMBER,
  FN_WIDTH       NUMBER,
  FC_NAME        VARCHAR2(200 BYTE),
  FL_DEL         NUMBER(1)                      DEFAULT 0,
  FK_SMID        INTEGER,
  FL_DIR_HEIGHT  NUMBER(1),
  FL_DIR_WIDTH   NUMBER(1),
  FL_FIRST       NUMBER(1)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TLABLOT_PARAM IS 'Cправочник штативов'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FK_ID IS 'FK_ID'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FN_HEIGHT IS 'Высота штатива'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FN_WIDTH IS 'Ширина штатива'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FC_NAME IS 'Наименование'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FL_DEL IS 'Признак удаления'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FK_SMID IS 'Значение по умолчанию.'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FL_DIR_HEIGHT IS 'По вертикали (0-сверху вниз; 1-снизу вверх)'
/

COMMENT ON COLUMN ASU.TLABLOT_PARAM.FL_DIR_WIDTH IS 'По горизонтали (0-слева направо; 1-справа налево)'
/


--
-- TLAB_LOT_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TLABLOT_PARAM (Table)
--
CREATE OR REPLACE TRIGGER ASU."TLAB_LOT_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON asu.TLABLOT_PARAM
 REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
BEGIN
    SELECT SEQ_LAB_ACCUMULATE.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
/
SHOW ERRORS;


