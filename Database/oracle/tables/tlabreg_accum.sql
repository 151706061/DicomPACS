/

COMMENT ON COLUMN ASU.TLABREG_ACCUM.FD_REGIST IS 'Дата регистрации'
/


--
-- TLABREG_ACCUM_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TLABREG_ACCUM (Table)
--
CREATE OR REPLACE TRIGGER ASU."TLABREG_ACCUM_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON ASU.TLABREG_ACCUM  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
BEGIN
    SELECT SEQ_LAB_ACCUMULATE.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
