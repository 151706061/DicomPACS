CREATE TABLE ASU.TLABREG_ACCUM
(
  FK_ID          NUMBER,
  FK_NAZID       NUMBER,
  FK_LABREG      NUMBER,
  FN_PROBE       NUMBER,
  FK_LOT_ACCUME  NUMBER,
  FK_SOTRID      NUMBER,
  FD_REGIST      DATE
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TLABREG_ACCUM.FK_ID IS 'ID'
/

COMMENT ON COLUMN ASU.TLABREG_ACCUM.FK_NAZID IS 'TNAZAN.FK_ID'
/

COMMENT ON COLUMN ASU.TLABREG_ACCUM.FK_LABREG IS 'TLABREG.FK_ID'
/

