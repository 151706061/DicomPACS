STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOPARALLEL
/


--
-- TLOGCAPTION_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TLOGCAPTION (Table)
--
CREATE OR REPLACE TRIGGER ASU."TLOGCAPTION_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON ASU.TLOGCAPTION  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
begin
    SELECT SEQ_TLOGCAPTION.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
end;
/
