--
-- TLOGCAPTION  (Table) 
--
CREATE TABLE ASU.TLOGCAPTION
(
  FK_ID         NUMBER,
  FC_ACTION     VARCHAR2(10 BYTE),
  FC_CAPTION    VARCHAR2(200 BYTE),
  FK_OWNER      NUMBER,
  FC_NAME       VARCHAR2(30 BYTE),
  FL_DEL        NUMBER,
  FC_TYPE       VARCHAR2(15 BYTE),
  FC_OWNERNAME  VARCHAR2(200 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FC_ACTION IS 'Действие (Update, Insert, Delete)'
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FC_CAPTION IS 'Расшифровка действия'
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FK_OWNER IS 'Родитель'
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FC_NAME IS 'Имя объекта логирования'
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FL_DEL IS 'Признак удаления'
/

COMMENT ON COLUMN ASU.TLOGCAPTION.FC_TYPE IS 'Тип объекта'
/


--
