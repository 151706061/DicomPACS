--
-- TLOGCAPTION_RIGHTS  (Table) 
--
CREATE TABLE ASU.TLOGCAPTION_RIGHTS
(
  FK_ID      NUMBER,
  FK_SOTRID  NUMBER,
  FK_LOGID   NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/


--
-- TLOGCAPT_RIGHTS_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TLOGCAPTION_RIGHTS (Table)
--
CREATE OR REPLACE TRIGGER ASU."TLOGCAPT_RIGHTS_BEFORE_INSERT" 
 BEFORE
 INSERT
 ON ASU.TLOGCAPTION_RIGHTS  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
BEGIN
    SELECT SEQ_TLOGCAPTION_RIGHTS.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
/
