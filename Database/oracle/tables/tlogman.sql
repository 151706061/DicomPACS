--
-- TLOGMAN  (Table) 
--
CREATE TABLE ASU.TLOGMAN
(
  SOTRID       VARCHAR2(255 BYTE),
  CHANGE_DATE  DATE,
  OSUSER       VARCHAR2(100 BYTE),
  MACHINE      VARCHAR2(100 BYTE),
  TERMINAL     VARCHAR2(100 BYTE),
  PROGRAM      VARCHAR2(100 BYTE),
  FC_COMMENT   VARCHAR2(255 BYTE),
  LOGON_TIME   DATE,
  LOGMANID     NUMBER                           NOT NULL,
  FK_OWNER     NUMBER,
  FC_PERIOD    VARCHAR2(255 BYTE)
)
TABLESPACE LOG
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          1040K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/


--
-- TLOGAN_INS  (Trigger) 
--
--  Dependencies: 
--   TLOGMAN (Table)
--
CREATE OR REPLACE TRIGGER ASU."TLOGAN_INS" 
 BEFORE 
 INSERT
 ON ASU.TLOGMAN  REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW
Begin 
  select SEQ_Tlogman.nextval into :new.logmanid from dual;
End;
