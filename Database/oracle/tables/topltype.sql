--
-- TOPLTYPE  (Table) 
--
CREATE TABLE ASU.TOPLTYPE
(
  FK_ID    NUMBER(16)                           NOT NULL,
  FC_NAME  VARCHAR2(30 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TOPLTYPE IS 'Справочник "Виды оплат" by TimurLan'
/

COMMENT ON COLUMN ASU.TOPLTYPE.FK_ID IS 'SEQUENCE=[SEQ_TOPLTYPE]'
/

COMMENT ON COLUMN ASU.TOPLTYPE.FC_NAME IS 'название'
/


--
-- TOPLTYPE_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TOPLTYPE (Table)
--
CREATE OR REPLACE TRIGGER ASU."TOPLTYPE_BEFORE_INSERT" 
BEFORE INSERT
ON ASU.TOPLTYPE REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  SELECT SEQ_TOPLTYPE.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
