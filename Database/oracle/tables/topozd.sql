--
-- TOPOZD  (Table) 
--
CREATE TABLE ASU.TOPOZD
(
  FK_ID       NUMBER(15),
  FC_NAME     VARCHAR2(100 BYTE),
  FN_ORDER    NUMBER(9),
  FL_DEFAULT  NUMBER(1)                         DEFAULT 0,
  FL_DEL      NUMBER(1)                         DEFAULT 0
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TOPOZD IS 'Справочник опозданий by TimurLan'
/

COMMENT ON COLUMN ASU.TOPOZD.FK_ID IS 'SEQUENCE=[SEQ_TOPOZD]'
/

COMMENT ON COLUMN ASU.TOPOZD.FC_NAME IS 'название'
/

COMMENT ON COLUMN ASU.TOPOZD.FN_ORDER IS 'порядок'
/

COMMENT ON COLUMN ASU.TOPOZD.FL_DEFAULT IS 'знач. по-умолчанию'
/

COMMENT ON COLUMN ASU.TOPOZD.FL_DEL IS 'признак удаления'
/


--
-- TOPOZD_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TOPOZD (Table)
--
CREATE OR REPLACE TRIGGER ASU."TOPOZD_BEFORE_INSERT" 
BEFORE INSERT
ON ASU.TOPOZD REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  SELECT SEQ_TOPOZD.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
