--
CREATE OR REPLACE TRIGGER ASU."TPAC_GROUP_BEFORE_DELETE" 
  BEFORE DELETE ON ASU.TPAC_GROUP   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin--by TimurLan 18/08/04
  DELETE FROM TBLOBS WHERE FK_ID = :OLD.FK_FILTFILE;
  DELETE FROM TBLOBS WHERE FK_ID = :OLD.FK_FILTDFILE;
End;
/
