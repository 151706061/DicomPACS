-- TPAC_GROUP  (Table) 
--
CREATE TABLE ASU.TPAC_GROUP
(
  FK_ID         NUMBER(15),
  FC_NAME       VARCHAR2(30 BYTE),
  FK_SOTRID     NUMBER(10),
  FC_SQLSTR     VARCHAR2(1000 BYTE),
  FC_COMMENT    VARCHAR2(250 BYTE),
  FK_FILTFILE   NUMBER(15),
  FK_FILTDFILE  NUMBER(15)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          160K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TPAC_GROUP.FK_ID IS 'SEQUENCE=[SEQ_TPAC_GROUP]'
/

COMMENT ON COLUMN ASU.TPAC_GROUP.FK_FILTFILE IS 'код из TBLOBS для загрузки файла'
/


