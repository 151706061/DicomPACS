CREATE OR REPLACE TRIGGER ASU."TPAC_GROUP_BEFORE_INSERT" 
BEFORE INSERT 
ON tpac_group
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
begin
 select SEQ_TPAC_GROUP.NEXTVAL into :new.fk_id from dual;
end;
