--
-- TPLAN_DELETE_ALL  (Trigger) 
--
--  Dependencies: 
--   TPLAN (Table)
--
CREATE OR REPLACE TRIGGER ASU."TPLAN_DELETE_ALL" 
AFTER DELETE
ON ASU.TPLAN REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  Delete from TPLANMAIN where FK_PLANID = :OLD.FK_ID;
end;
/
SHOW ERRORS;


--
-- TPLAN_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TPLAN (Table)
--
CREATE OR REPLACE TRIGGER ASU."TPLAN_BEFORE_INSERT" 
BEFORE INSERT
ON ASU.TPLAN REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  SELECT SEQ_TPLAN.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
SHOW ERRORS;


