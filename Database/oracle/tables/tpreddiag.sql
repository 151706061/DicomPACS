--
-- TPREDDIAG  (Table) 
--
CREATE TABLE ASU.TPREDDIAG
(
  FK_ID       NUMBER(15),
  FK_PACID    NUMBER(15),
  FK_SMID     NUMBER(15),
  FK_VRACHID  NUMBER(15),
  FD_DATA     DATE
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TPREDDIAG.FK_ID IS 'SEQUENCE=[SEQ_TEPID]'
/


--
-- TPREDDIAG_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TPREDDIAG (Table)
--
CREATE OR REPLACE TRIGGER ASU."TPREDDIAG_BEFORE_INSERT" 
  BEFORE INSERT
  ON ASU.TPREDDIAG   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  SELECT SEQ_TEPID.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
