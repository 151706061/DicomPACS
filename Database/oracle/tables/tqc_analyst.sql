--
-- TQC_ANALYST  (Table) 
--
CREATE TABLE ASU.TQC_ANALYST
(
  FK_ID       NUMBER                            NOT NULL,
  FC_NAME     VARCHAR2(255 BYTE),
  FN_CVI      NUMBER(18,5),
  FN_CVG      NUMBER(18,5),
  FN_TEA      NUMBER(18,5),
  FN_MAXCV10  NUMBER(18,5),
  FN_MAXB10   NUMBER(18,5),
  FN_MAXCV20  NUMBER(18,5),
  FN_MAXB20   NUMBER(18,5),
  FL_DEL      NUMBER                            DEFAULT 0,
  FK_SMID     NUMBER,
  FN_SORT     NUMBER
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FK_ID IS 'ID'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FC_NAME IS 'Наименование'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_CVI IS 'CVi'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_CVG IS 'CVg'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_TEA IS 'TEa'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_MAXCV10 IS 'CV10'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_MAXB10 IS 'B10'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_MAXCV20 IS 'CV20'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FN_MAXB20 IS 'B20'
/

COMMENT ON COLUMN ASU.TQC_ANALYST.FK_SMID IS 'TSMID.FK_ID'
/


--
-- TQC_ANALYST_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TQC_ANALYST (Table)
--
CREATE OR REPLACE TRIGGER ASU."TQC_ANALYST_BEFORE_INSERT" 
 BEFORE
  INSERT
 ON ASU.TQC_ANALYST REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
    SELECT SEQ_QC_LAB.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
/
