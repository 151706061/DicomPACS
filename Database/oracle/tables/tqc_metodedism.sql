            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/


--
-- TQC_METODEDISM_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TQC_METODEDISM (Table)
--
CREATE OR REPLACE TRIGGER ASU."TQC_METODEDISM_BEFORE_INSERT" 
 BEFORE
  INSERT
 ON tqc_metodedism
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
    SELECT SEQ_QC_LAB.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
/
