CREATE TABLE ASU.TQC_METODEDISM
(
  FK_ID    NUMBER                               NOT NULL,
  FC_NAME  VARCHAR2(30 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
