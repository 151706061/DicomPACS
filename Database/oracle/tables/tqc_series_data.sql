--
-- TQC_SERIES_DATA  (Table) 
--
CREATE TABLE ASU.TQC_SERIES_DATA
(
  FK_ID        NUMBER                           NOT NULL,
  FN_NUMBER    NUMBER,
  FK_SERIESID  INTEGER,
  FD_DATE      DATE,
  FN_RESULT    NUMBER(18,4),
  FC_COMMENT   VARCHAR2(255 BYTE),
  FL_ACTIVE    NUMBER                           DEFAULT 0,
  FL_DEL       NUMBER                           DEFAULT 0,
  FK_SOTRID    NUMBER,
  FL_EXPORT    NUMBER                           DEFAULT 0
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FK_ID IS 'ID'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FN_NUMBER IS 'Порядковый номер теста'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FK_SERIESID IS 'TQC_SERIES.FK_ID'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FD_DATE IS 'Дата внесения результата'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FN_RESULT IS 'результат'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FC_COMMENT IS 'комментарий'
/

COMMENT ON COLUMN ASU.TQC_SERIES_DATA.FL_EXPORT IS '0 - результат не передан. 1 - результат передан'
/


--
-- TQC_SERIES_DATA_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TQC_SERIES_DATA (Table)
--
CREATE OR REPLACE TRIGGER ASU."TQC_SERIES_DATA_BEFORE_INSERT" 
 BEFORE
  INSERT
 ON ASU.TQC_SERIES_DATA REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
    SELECT SEQ_QC_LAB.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
END;
/
