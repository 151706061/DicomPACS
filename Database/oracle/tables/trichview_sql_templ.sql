DROP TABLE ASU.TRICHVIEW_SQL_TEMPL CASCADE CONSTRAINTS
/

--
-- TRICHVIEW_SQL_TEMPL  (Table) 
--
CREATE TABLE ASU.TRICHVIEW_SQL_TEMPL
(
  FK_ID    NUMBER,
  FC_NAME  VARCHAR2(255 BYTE),
  FB_BLOB  BLOB,
  FN_TYPE  NUMBER                               DEFAULT 0
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOLOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TRICHVIEW_SQL_TEMPL.FN_TYPE IS '0 - функция до открытия, 1 - функция после сохранения'
/


--
-- SEQ_TRICHVIEW_SQL_TEMPL#BI  (Trigger) 
--
--  Dependencies: 
--   TRICHVIEW_SQL_TEMPL (Table)
--
CREATE OR REPLACE TRIGGER ASU.seq_trichview_sql_templ#bi
 BEFORE
  INSERT
 ON ASU.TRICHVIEW_SQL_TEMPL REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
    IF :NEW.FK_ID IS NULL OR (:NEW.FK_ID < 0) THEN
       SELECT SEQ_TRICHVIEW_SQL_TEMPL.nextval into :NEW.FK_ID FROM DUAL;
    END IF;
END;
/
SHOW ERRORS;


