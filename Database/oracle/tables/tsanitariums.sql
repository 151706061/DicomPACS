--
-- TSANITARIUMS  (Table) 
--
CREATE TABLE ASU.TSANITARIUMS
(
  FK_ID    NUMBER(9)                            DEFAULT -1,
  FC_NAME  VARCHAR2(20 BYTE)
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON TABLE ASU.TSANITARIUMS IS 'Санатории для переводов by TimurLan'
/

COMMENT ON COLUMN ASU.TSANITARIUMS.FK_ID IS 'SEQUENCE=[SEQ_TSANITARIUMS]'
/

COMMENT ON COLUMN ASU.TSANITARIUMS.FC_NAME IS 'Наименование санатория'
/


--
-- TSANITARIUMS_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TSANITARIUMS (Table)
--
CREATE OR REPLACE TRIGGER ASU."TSANITARIUMS_BEFORE_INSERT" 
BEFORE INSERT
ON ASU.TSANITARIUMS REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
Begin
  SELECT SEQ_TSANITARIUMS.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
