(
  FK_ID         NUMBER(9)                       DEFAULT 0,
  FK_SPECID     NUMBER(9)                       DEFAULT -1,
  FL_PODPIS     NUMBER(1)                       DEFAULT 0,
  FL_EDITSPR    NUMBER(1)                       DEFAULT 0,
  FL_PARAMMEST  NUMBER(1)                       DEFAULT 0
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
