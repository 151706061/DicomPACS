 BEFORE
  INSERT
 ON ttherpoints_terr
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
begin
  SELECT SEQ_TTHERPOINTS.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
end;
