--
-- TTYPESROK  (Table) 
--
CREATE TABLE ASU.TTYPESROK
(
  FK_ID    NUMBER(16)                           NOT NULL,
  FC_NAME  VARCHAR2(30 BYTE),
  FK_TYPE  NUMBER(1)                            DEFAULT 1
)
TABLESPACE USR
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          520K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING
/

COMMENT ON COLUMN ASU.TTYPESROK.FK_ID IS 'SEQUENCE=[SEQ_TSROKTYPE]'
/


--
-- TSROKTYPE_BEFORE_INSERT  (Trigger) 
--
--  Dependencies: 
--   TTYPESROK (Table)
--
CREATE OR REPLACE TRIGGER ASU."TSROKTYPE_BEFORE_INSERT" 
  BEFORE INSERT
  ON ASU.TTYPESROK   REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
Begin
  SELECT SEQ_TSROKTYPE.NEXTVAL INTO :NEW.FK_ID FROM DUAL;
End;
/
